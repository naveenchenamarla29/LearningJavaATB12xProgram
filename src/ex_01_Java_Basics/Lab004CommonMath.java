package ex_01_Java_Basics;

public class Lab004CommonMath {
    public static void main(String[] args) {
        System.out.println(2+2);
        System.out.println(2-2);
        System.out.println(2*2);
        System.out.println(4/2); //Prints quotient
        System.out.println(3/2); //Prints quotient. It prints integer values only but not 1.5
        System.out.println(4%2); //Prints remainder
        System.out.println(3%2); //Prints remainder
    }
}
