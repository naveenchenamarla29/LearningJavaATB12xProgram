package ex_04_Operators;

public class Lab029_CompundOperator {
    public static void main(String[] args) {
        int a = 10;

        //This is same as a = a + 10
        a += 10;
        System.out.println(a);

        //this is same as a = a - 5
        a -= 5;
        System.out.println(a);

        //this is same as a = a/5
        a /= 5;
        System.out.println(a);

    }
}
