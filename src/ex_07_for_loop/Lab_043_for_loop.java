package ex_07_for_loop;

public class Lab_043_for_loop {
    public static void main(String[] args) {

        //Print numbers from 1 to 10

        //It first initializes, then checks for condition and then go to the code block inside for loop
        //And then after code block execution, it increments and then checks condition again
        //And then goes to code block and increments again, then condition and it goes on this cycle
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
